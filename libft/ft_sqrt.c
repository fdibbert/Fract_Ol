#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;
	int c;

	c = 0;
	i = 1;
	while (i <= nb / i)
	{
		c = i * i;
		if (c == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
